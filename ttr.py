from pyrogram import Client, filters
from pyrogram.types import Message
import time

# ---------- CONFIG ----------
API_ID = 29680263              # ‡¶§‡ßã‡¶Æ‡¶æ‡¶∞ Telegram API ID
API_HASH = "a251c8203284c9fe7812f418ec8aa3a9"  # ‡¶§‡ßã‡¶Æ‡¶æ‡¶∞ Telegram API HASH
SESSION = "my_account"         # Pyrogram session name (‡¶Ø‡ßá‡¶Æ‡¶® "my_account")
OWNER_ID = 6577308099         # ‡¶§‡ßã‡¶Æ‡¶æ‡¶∞ numeric Telegram ID
COOLDOWN_SECONDS = 300         # ‡ß´ ‡¶Æ‡¶ø‡¶®‡¶ø‡¶ü cooldown

# ---------- VARIABLES ----------
sleep_mode = False
cooldowns = {}  # user_id : last_active_time

# ---------- INIT CLIENT ----------
app = Client(
    SESSION,
    api_id=API_ID,
    api_hash=API_HASH
)

# ---------- OFFLINE MESSAGE ----------
OFFLINE_MSG = (
    "üí≠ I‚Äôm offline for a while...\n"
    "‚è∏Ô∏è No replies, no calls ‚Äî just a little peace and silence.\n"
    "üì© If it‚Äôs important, leave your message. I‚Äôll be back when I‚Äôm meant to.\n\n"
    "üìù This text was automatically generated by Docker"
)

# ===== COMMANDS =====
@app.on_message(filters.command("sleep") & filters.user(OWNER_ID))
async def activate_sleep(_, msg: Message):
    global sleep_mode
    sleep_mode = True
    await msg.reply_text("üò¥ Sleep mode activated. Auto-reply is now ON.")

@app.on_message(filters.command("off") & filters.user(OWNER_ID))
async def deactivate_sleep(_, msg: Message):
    global sleep_mode
    sleep_mode = False
    await msg.reply_text("‚úÖ Sleep mode deactivated. Auto-reply is now OFF.")

@app.on_message(filters.command("status") & filters.user(OWNER_ID))
async def status_check(_, msg: Message):
    status = "ON" if sleep_mode else "OFF"
    await msg.reply_text(f"‚ÑπÔ∏è Auto-reply status: {status}")

# ===== MARK SELF ACTIVE =====
@app.on_message(filters.me)
async def mark_active(_, msg: Message):
    """‡¶Ø‡¶ñ‡¶® ‡¶§‡ßÅ‡¶Æ‡¶ø ‡¶®‡¶ø‡¶ú‡ßá ‡¶Æ‡ßá‡¶∏‡ßá‡¶ú ‡¶™‡¶æ‡¶†‡¶æ‡¶¨‡ßá, cooldown reset ‡¶π‡¶¨‡ßá‡•§"""
    cooldowns[msg.chat.id] = time.time()

# ===== AUTO-REPLY (PRIVATE ONLY) =====
@app.on_message(filters.private)
async def auto_reply(_, msg: Message):
    global sleep_mode
    if not sleep_mode:
        return

    # ‡¶®‡¶ø‡¶ú‡ßá‡¶∞ ‡¶Æ‡ßá‡¶∏‡ßá‡¶ú ignore
    if msg.from_user and msg.from_user.is_self:
        return

    # bot ignore
    if msg.from_user and msg.from_user.is_bot:
        return

    try:
        user_id = msg.chat.id
        now = time.time()
        last_time = cooldowns.get(user_id, 0)

        # cooldown check
        if now - last_time < COOLDOWN_SECONDS:
            return

        # set last reply time
        cooldowns[user_id] = now
        await msg.reply_text(OFFLINE_MSG)

    except Exception as e:
        print("Auto-reply skipped due to:", e)

# ===== MAIN =====
if __name__ == "__main__":
    print("üíª Userbot running in background... Use /sleep, /off, /status from your Telegram account.")
    app.run()
